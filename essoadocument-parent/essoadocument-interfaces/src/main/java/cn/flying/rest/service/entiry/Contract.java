package cn.flying.rest.service.entiry;

public class Contract {

  private int id;
  private String escontractname;
  private String escontractnum;
  private String esprojectname;
  private String esdevice;
  private String escompany;
  private String esperson;
  private String esperstel;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getEscontractname() {
    return escontractname;
  }

  public void setEscontractname(String escontractname) {
    this.escontractname = escontractname;
  }

  public String getEscontractnum() {
    return escontractnum;
  }

  public void setEscontractnum(String escontractnum) {
    this.escontractnum = escontractnum;
  }

  public String getEsprojectname() {
    return esprojectname;
  }

  public void setEsprojectname(String esprojectname) {
    this.esprojectname = esprojectname;
  }

  public String getEsdevice() {
    return esdevice;
  }

  public void setEsdevice(String esdevice) {
    this.esdevice = esdevice;
  }

  public String getEscompany() {
    return escompany;
  }

  public void setEscompany(String escompany) {
    this.escompany = escompany;
  }

  public String getEsperson() {
    return esperson;
  }

  public void setEsperson(String esperson) {
    this.esperson = esperson;
  }

  public String getEsperstel() {
    return esperstel;
  }

  public void setEsperstel(String esperstel) {
    this.esperstel = esperstel;
  }

}
